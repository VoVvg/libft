#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	char	*ptr;
	char	*ptr2;

	ptr = dst;
	ptr2 = (char *)src;
	if (n == 0 || ((unsigned char *)dst == (unsigned char *)src))
		return (dst);
	while (n--)
		*(ptr++) = *(ptr2++);
	return (dst);
}
